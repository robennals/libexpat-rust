//! SipHash-2-4 implementation for hash-table randomization.
//!
//! Ported from expat's `siphash.h` (original C by William Ahern, derived from
//! Aumasson & Bernstein). Used to randomize hash seeds and protect against
//! hash-collision denial-of-service attacks on attribute/element name tables.

/// Perform a left rotate on a 64-bit value
#[inline]
fn rotl64(x: u64, b: u32) -> u64 {
    x.rotate_left(b)
}

/// Parse a 64-bit little-endian value from 8 bytes
fn u8_to_u64_le(bytes: &[u8]) -> u64 {
    assert!(bytes.len() >= 8, "Not enough bytes");
    u64::from_le_bytes([
        bytes[0], bytes[1], bytes[2], bytes[3], bytes[4], bytes[5], bytes[6], bytes[7],
    ])
}

/// A SipHash state machine for incremental hashing
pub struct SipHasher {
    v0: u64,
    v1: u64,
    v2: u64,
    v3: u64,
    buf: [u8; 8],
    buf_len: usize,
    msg_byte_count: u64,
}

impl SipHasher {
    /// Create a new SipHasher with the given 16-byte key
    pub fn new(key: &[u8; 16]) -> Self {
        let k0 = u8_to_u64_le(&key[0..8]);
        let k1 = u8_to_u64_le(&key[8..16]);

        let v0 = 0x736f6d6570736575u64 ^ k0;
        let v1 = 0x646f72616e646f6du64 ^ k1;
        let v2 = 0x6c7967656e657261u64 ^ k0;
        let v3 = 0x7465646279746573u64 ^ k1;

        SipHasher {
            v0,
            v1,
            v2,
            v3,
            buf: [0u8; 8],
            buf_len: 0,
            msg_byte_count: 0,
        }
    }

    /// Perform a SipRound with the specified number of rounds
    fn round(&mut self, rounds: usize) {
        for _ in 0..rounds {
            self.v0 = self.v0.wrapping_add(self.v1);
            self.v1 = rotl64(self.v1, 13);
            self.v1 ^= self.v0;
            self.v0 = rotl64(self.v0, 32);

            self.v2 = self.v2.wrapping_add(self.v3);
            self.v3 = rotl64(self.v3, 16);
            self.v3 ^= self.v2;

            self.v0 = self.v0.wrapping_add(self.v3);
            self.v3 = rotl64(self.v3, 21);
            self.v3 ^= self.v0;

            self.v2 = self.v2.wrapping_add(self.v1);
            self.v1 = rotl64(self.v1, 17);
            self.v1 ^= self.v2;
            self.v2 = rotl64(self.v2, 32);
        }
    }

    /// Process a full 8-byte block through the compressor
    fn process_block(&mut self, m: u64) {
        self.v3 ^= m;
        self.round(2);
        self.v0 ^= m;
    }

    /// Write data into the hasher
    pub fn write(&mut self, data: &[u8]) {
        let mut remaining = data;

        while !remaining.is_empty() {
            // Fill the buffer
            let can_write = 8 - self.buf_len;
            let to_write = std::cmp::min(can_write, remaining.len());

            self.buf[self.buf_len..self.buf_len + to_write].copy_from_slice(&remaining[..to_write]);
            self.buf_len += to_write;
            remaining = &remaining[to_write..];

            // If buffer is full, process it
            if self.buf_len == 8 {
                let m = u8_to_u64_le(&self.buf);
                self.process_block(m);
                self.buf_len = 0;
                self.msg_byte_count += 8;
            }
        }
    }

    /// Finish hashing and return the result
    pub fn finish(mut self) -> u64 {
        // Compute b: (msg_len + buf_len) << 56 | remaining bytes
        let left = self.buf_len as u8;
        let total_len = self.msg_byte_count + left as u64;
        let mut b = total_len << 56;

        // Add the remaining bytes in little-endian order
        if left == 7 {
            b |= (self.buf[6] as u64) << 48;
        }
        if left >= 6 {
            b |= (self.buf[5] as u64) << 40;
        }
        if left >= 5 {
            b |= (self.buf[4] as u64) << 32;
        }
        if left >= 4 {
            b |= (self.buf[3] as u64) << 24;
        }
        if left >= 3 {
            b |= (self.buf[2] as u64) << 16;
        }
        if left >= 2 {
            b |= (self.buf[1] as u64) << 8;
        }
        if left >= 1 {
            b |= self.buf[0] as u64;
        }

        // Finalization
        self.v3 ^= b;
        self.round(2);
        self.v0 ^= b;
        self.v2 ^= 0xff;
        self.round(4);

        self.v0 ^ self.v1 ^ self.v2 ^ self.v3
    }
}

/// One-shot SipHash-2-4 computation
///
/// # Arguments
/// * `key` - 16-byte key
/// * `data` - Message to hash
///
/// # Returns
/// The 64-bit SipHash digest
pub fn sip_hash(key: &[u8; 16], data: &[u8]) -> u64 {
    let mut hasher = SipHasher::new(key);
    hasher.write(data);
    hasher.finish()
}

