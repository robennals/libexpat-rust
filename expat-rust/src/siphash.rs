    #[test]
    fn test_siphash_struct_api() {
        let key: [u8; 16] = [
            0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d,
            0x0e, 0x0f,
        ];

        // Test with a simple message
        let msg = b"hello world";
        let result1 = sip_hash(&key, msg);

        let mut hasher = SipHasher::new(&key);
        hasher.write(msg);
        let result2 = hasher.finish();

        assert_eq!(result1, result2);
    }

    #[test]
    fn test_siphash_incremental() {
        let key: [u8; 16] = [
            0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d,
            0x0e, 0x0f,
        ];

        let msg = b"The quick brown fox jumps over the lazy dog";

        // All at once
        let result1 = sip_hash(&key, msg);

        // Incrementally
        let mut hasher = SipHasher::new(&key);
        hasher.write(&msg[..10]);
        hasher.write(&msg[10..20]);
        hasher.write(&msg[20..]);
        let result2 = hasher.finish();

        assert_eq!(result1, result2);
    }

